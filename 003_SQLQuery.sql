/*ЗАДАНИЕ 1.
Напишите SQL-запрос, который выбирает из таблицы M_PHONES только те строки, у которых значение столбца
body_type равно 'раскладной’ . В результат запроса вывести все столбцы таблицы*/

SELECT
	*
FROM m_phones
WHERE body_type = 'раскладной'

/*ЗАДАНИЕ 2.
Напишите SQL-запрос, который выбирает из таблицы M_PHONES только те строки, у которых значение столбца
body_type может быть равно 'раскладной' или ' слайдер ' . В результат запроса вывести все столбцы таблицы.*/

SELECT
	*
FROM m_phones
WHERE body_type IN ('раскладной', 'слайдер')

/*ЗАДАНИЕ 3.
Напишите SQL-запрос, который выбирает из таблицы M_PHONES только те строки, у которых значение столбца
body_type равно 'раскладной' и значение столбца battery_capacity больше чем 1000. Показать все столбцы таблицы*/

SELECT
	*
FROM m_phones
WHERE
	body_type = 'раскладной'
	AND battery_capacity > 1000

/*ЗАДАНИЕ 4.
