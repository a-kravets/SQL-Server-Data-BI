body_type равно 'раскладной' и значение столбца battery_capacity менше или равно 750. Показать все столбцы
таблицы*/

SELECT
	*
FROM m_phones
WHERE
	body_type = 'раскладной'
	AND battery_capacity <= 750

/*ЗАДАНИЕ 5.
Напишите SQL-запрос, который выбирает из таблицы M_PHONES только те строки, у которых значение столбца
body_type равно 'раскладной' и значение столбца battery_capacity может быть от 800 до 1000 включительно.
Показать все столбцы таблицы*/

SELECT
	*
FROM m_phones
WHERE
	body_type = 'раскладной'
	AND battery_capacity BETWEEN 800 AND 1000

/*ЗАДАНИЕ 6.
Напишите SQL-запрос, который выбирает из таблицы M_PHONES только те строки, у которых значение столбца body_type
равно 'раскладной' и значение столбца battery_capacity может быть или 600 или 1500. Показать все столбцы таблицы.
Использовать оператор IN()*/

SELECT
	*
FROM m_phones
WHERE
	body_type = 'раскладной'
	AND battery_capacity IN (600, 1500)

/*ЗАДАНИЕ 7.
